package domain;

public class LoanParameters {
	
	private int instellmentCount;
	private int amount;
	
	public int getInstellmentCount() {
		return instellmentCount;
	}
	public void setInstellmentCount(int instellmentCount) {
		this.instellmentCount = instellmentCount;
	}
	public int getAmount() {
		return amount;
	}
	public void setAmount(int amount) {
		this.amount = amount;
	}
}
